      public void filtrarDatos(String valor) {
        String SQL = "";
        String [] titulos={"Id", "Nombre", "Apellidos", "Email"};
        String [] registros=new String[7];
        
        DefaultTableModel modelo=new DefaultTableModel(null,titulos);
        
        if (jRadioButton1.isSelected()){
             SQL="select * from clientes where nombre like '%"+valor+"%'"; 
        }else{
            if(jRadioButton2.isSelected()){
              SQL="select * from clientes where apellidos like '%"+valor+"%'"; 
            }
            else{
                 SQL="select * from clientes where email like '%"+valor+"%'"; 
            }
        }
        
        
        
       
        
        try {
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            
                    
            
            
          while (rs.next())  {
              
           registros[0]=rs.getString("idcliente");
           registros[1]=rs.getString("nombre");
           registros[2]=rs.getString("apellidos");
           registros[3]=rs.getString("email");
          
           
           modelo.addRow(registros);
           
              
          }
          
          tabladatos.setModel(modelo);
          
          
          
          
        }catch (SQLException e) {
            
         JOptionPane.showMessageDialog(null, "Error al Mostrar Datos"+  e.getMessage());
         
        }
        
        
        
    }
    
